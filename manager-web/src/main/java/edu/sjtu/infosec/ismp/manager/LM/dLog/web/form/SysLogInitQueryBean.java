package edu.sjtu.infosec.ismp.manager.LM.dLog.web.form;

import java.io.Serializable;
import java.util.List;
import java.util.TreeMap;

public class SysLogInitQueryBean implements Serializable {

	private static final long serialVersionUID = 5999473103399148052L;
	
	private List<?> domain;
	
	private TreeMap<Integer,String> facility;
	
	private String beginDate;
	
	private String endDate;
	
	private TreeMap<Integer,String> severity;
	
	private String query;

	public List<?> getDomain() {
		return domain;
	}

	public void setDomain(List<?> domain) {
		this.domain = domain;
	}

	public String getBeginDate() {
		return beginDate;
	}

	public void setBeginDate(String beginDate) {
		this.beginDate = beginDate;
	}

	public String getEndDate() {
		return endDate;
	}

	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}

	public TreeMap<Integer, String> getSeverity() {
		return severity;
	}

	public TreeMap<Integer, String> getFacility() {
		return facility;
	}

	public void setSeverity(TreeMap<Integer, String> severity) {
		this.severity = severity;
	}

	public void setFacility(TreeMap<Integer, String> facility) {
		this.facility = facility;
	}

	public String getQuery() {
		return query;
	}

	public void setQuery(String query) {
		this.query = query;
	}
	
	
}
