package edu.sjtu.infosec.ismp.manager.LM.util.modle;

import java.io.Serializable;

public class StringOptimize implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 3929414189092484113L;
	
	//原始字符串的长度
	private Integer stringSize;
	
	//需要优化的长度
	private Integer optimizeSize;
	
	//原始字符串
	private String stringOptimize;
	
	//优化后的字符串
	private String stringOptimizeLater;

	public Integer getStringSize() {
		return stringSize;
	}

	public void setStringSize(Integer stringSize) {
		this.stringSize = stringSize;
	}

	public Integer getOptimizeSize() {
		return optimizeSize;
	}

	public void setOptimizeSize(Integer optimizeSize) {
		this.optimizeSize = optimizeSize;
	}

	public String getStringOptimize() {
		return stringOptimize;
	}

	public void setStringOptimize(String stringOptimize) {
		this.stringOptimize = stringOptimize;
	}

	public String getStringOptimizeLater() {
		return stringOptimizeLater;
	}

	public void setStringOptimizeLater(String stringOptimizeLater) {
		this.stringOptimizeLater = stringOptimizeLater;
	}
	
}
