package edu.sjtu.infosec.ismp.manager.AM.web.form;

import org.apache.struts.action.ActionForm;

import edu.sjtu.infosec.ismp.manager.AM.model.AssetDeviceBO;
import edu.sjtu.infosec.ismp.manager.AM.model.AssetHardwareBO;
import edu.sjtu.infosec.ismp.manager.AM.model.AssetPositionBO;
import edu.sjtu.infosec.ismp.manager.AM.model.AssetSoftwareBO;


@SuppressWarnings("serial")
public class SearchForm extends ActionForm {
	 private AssetDeviceBO device = new AssetDeviceBO();
	 private AssetSoftwareBO software = new AssetSoftwareBO();
	 private AssetHardwareBO hardware = new AssetHardwareBO();
	 private AssetPositionBO position = new AssetPositionBO();
	 
	public AssetDeviceBO getDevice() {
		return device;
	}
	public void setDevice(AssetDeviceBO device) {
		this.device = device;
	}
	public AssetSoftwareBO getSoftware() {
		return software;
	}
	public void setSoftware(AssetSoftwareBO software) {
		this.software = software;
	}
	public AssetHardwareBO getHardware() {
		return hardware;
	}
	public void setHardware(AssetHardwareBO hardware) {
		this.hardware = hardware;
	}
	public AssetPositionBO getPosition() {
		return position;
	}
	public void setPosition(AssetPositionBO position) {
		this.position = position;
	} 
}
