package org.infosec.ismp.agent.comm.winsensor.model.operation;

import java.io.Serializable;
import java.sql.Timestamp;
import java.util.ArrayList;
import java.util.List;

/**
 * @author Rocky
 * @version create time：Dec 20, 2010 7:59:53 PM
 * 
 */
public class DutyManager implements Serializable {

	private static final long serialVersionUID = -3091850067764695219L;
	
	private String id;

	private List<Duty> duties = new ArrayList<Duty>();
	
	private String beginDate;		//值班开始时间
	
	private String endDate;		//值班结束时间
	
	private String complaintNumber;		//值班电话
	
	private String domainId;		//Domain id.
	
	private Timestamp createTime;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public List<Duty> getDuties() {
		return duties;
	}

	public void setDuties(List<Duty> duties) {
		this.duties = duties;
	}

	public String getBeginDate() {
		return beginDate;
	}

	public void setBeginDate(String beginDate) {
		this.beginDate = beginDate;
	}

	public String getEndDate() {
		return endDate;
	}

	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}

	public String getComplaintNumber() {
		return complaintNumber;
	}

	public void setComplaintNumber(String complaintNumber) {
		this.complaintNumber = complaintNumber;
	}

	public String getDomainId() {
		return domainId;
	}

	public void setDomainId(String domainId) {
		this.domainId = domainId;
	}

	public Timestamp getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Timestamp createTime) {
		this.createTime = createTime;
	}
}
