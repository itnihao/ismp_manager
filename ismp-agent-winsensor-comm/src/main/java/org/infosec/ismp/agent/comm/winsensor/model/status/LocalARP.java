public class LocalARP implements Serializable {

	private static final long serialVersionUID = -3825193639888646464L;
	
	private String localIp;

	/*
	 * Target ipAddress.
	 */
	private String ip;
	
	/*
	 * Target macAddress.
	 */
	private String mac;
	
	private String type;

	public String getLocalIp() {
		return localIp;
	}

	public void setLocalIp(String localIp) {
		this.localIp = localIp;
	}

	public String getIp() {
		return ip;
	}

	public void setIp(String ip) {
		this.ip = ip;
	}

	public String getMac() {
		return mac;
	}

	public void setMac(String mac) {
		this.mac = mac;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
}
