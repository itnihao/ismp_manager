package org.infosec.ismp.agent.comm.winsensor.model.status;

import java.io.Serializable;

/**
 * @author Rocky
 * @version create time：Oct 13, 2010 10:05:17 AM
 * 
 */
public class NetworkStatus implements Serializable {

	private static final long serialVersionUID = -5381883681338622266L;

	/*
	 * 描叙信息
	 */
	private String description;
	
	/*
	 * IP地址
	 */
	private String[] ipAddress;
	
	/*
	 * 子网掩码
	 */
	private String[] ipSubnet;
	
	/*
	 * 网关
	 */
	private String[] gateway;
	
	/*
	 * MAC地址
	 */
	private String mac;
	
	/*
	 * DNS地址
	 */
	private String[] dns;
	
	/*
	 * 带宽
	 */
	private Double bandWidth;
	
	/*
	 * 是否被启用
	 */
	private Boolean iPEnabled;
	
	/*
	 * 接收包数
	 */
	private Long recPacket;
	
	/*
	 * 发送包数
	 */
	private Long sendPacket;
	
	/*
	 * 接收字节数/秒
	 */
	private Long recBytesPreSec;
	
	/*
	 * 发送字节数/秒
	 */
	private Long sendBytesPreSec;

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String[] getIpAddress() {
		return ipAddress;
	}

	public void setIpAddress(String[] ipAddress) {
		this.ipAddress = ipAddress;
	}

	public String[] getIpSubnet() {
		return ipSubnet;
	}

	public void setIpSubnet(String[] ipSubnet) {
		this.ipSubnet = ipSubnet;
	}

	public String[] getGateway() {
		return gateway;
	}

	public void setGateway(String[] gateway) {
		this.gateway = gateway;
	}

	public String getMac() {
		return mac;
	}

	public void setMac(String mac) {
		this.mac = mac;
	}

	public String[] getDns() {
		return dns;
	}

	public void setDns(String[] dns) {
		this.dns = dns;
	}

	public Double getBandWidth() {
		return bandWidth;
	}

	public void setBandWidth(Double bandWidth) {
		this.bandWidth = bandWidth;
	}

	public Boolean getIPEnabled() {
		return iPEnabled;
	}

	public void setIPEnabled(Boolean enabled) {
		iPEnabled = enabled;
	}

	public Long getRecPacket() {
		return recPacket;
	}

	public void setRecPacket(Long recPacket) {
		this.recPacket = recPacket;
	}

	public Long getSendPacket() {
		return sendPacket;
	}

	public void setSendPacket(Long sendPacket) {
		this.sendPacket = sendPacket;
	}

	public Long getRecBytesPreSec() {
		return recBytesPreSec;
	}

	public void setRecBytesPreSec(Long recBytesPreSec) {
		this.recBytesPreSec = recBytesPreSec;
	}

	public Long getSendBytesPreSec() {
		return sendBytesPreSec;
	}

	public void setSendBytesPreSec(Long sendBytesPreSec) {
		this.sendBytesPreSec = sendBytesPreSec;
	}
}
