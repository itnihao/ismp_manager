package org.infosec.ismp.agent.comm.winsensor.model.status;

import java.io.Serializable;

/**
 * @author Rocky
 * @version create time：Oct 13, 2010 9:50:17 AM
 * 
 */
public class PartitionStatus implements Serializable {

	private static final long serialVersionUID = -7199569569350092083L;

	/*
	 * 分区名
	 */
	private String name;
	
	/*
	 * 分区大小
	 */
	private Long size;
	
	/*
	 * 已使用大小
	 */
	private Long used;
	
	/*
	 * 文件系统
	 */
	private String fileSystem;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Long getSize() {
		return size;
	}

	public void setSize(Long size) {
		this.size = size;
	}

	public Long getUsed() {
		return used;
	}

	public void setUsed(Long used) {
		this.used = used;
	}

	public String getFileSystem() {
		return fileSystem;
	}

	public void setFileSystem(String fileSystem) {
		this.fileSystem = fileSystem;
	}
}
