package edu.sjtu.infosec.monitor.db.model;
/**
 * 表空间使用情况
 * @author XiongYu
 *
 */
public class TableSpaceUser {

	private String name;					//表空间名称
	private double size;					//总大小(单位：MB)
	private double used;					//已使用的大小(单位：MB)
	private String usedPercent;				//已使用百分比
	private double free;					//空闲空间大小（单位：MB）
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getSize() {
		return size;
	}
	public void setSize(double size) {
		this.size = size;
	}
	public double getUsed() {
		return used;
	}
	public void setUsed(double used) {
		this.used = used;
	}
	public String getUsedPercent() {
		return usedPercent;
	}
	public void setUsedPercent(String usedPercent) {
		this.usedPercent = usedPercent;
	}
	public double getFree() {
		return free;
	}
	public void setFree(double free) {
		this.free = free;
	}
	
	
	
}
