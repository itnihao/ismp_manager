package org.infosec.ismp.manager.rmi.event.dao.queryCondition;

import java.sql.Timestamp;

import org.infosec.ismp.manager.rmi.event.util.Page;

/**
 * Eventrealdisp的查询条件
 * @author wudengke 2009-6-29
 *
 */
public class EventrealdispCondition {
	
	/**
	 * 事件启始时间
	 */
	private Timestamp starttime;
	
	/**
	 * 事件结束时间
	 */
	private Timestamp endtime;

	/**
	 * 事件源IP地址
	 */
	private String src_ip;

	/**
	 * 事件源端口
	 */
	private Integer src_port;

	/**
	 * 事件目的端口
	 */
	private Integer dest_port;

	/**
	 * 事件的威胁等级
	 */
	private Integer thre_rank;

	/**
	 * 安全设备IP，唯一标识一个设备
	 */
	private String faci_ip;

	/**
	 * 安全设备类型
	 */
	private String faci_type;

	/**
	 * 事件类型
	 */
	private String event_type;

	/**
	 * 事件描述
	 */
	private String descrip;

	/**
	 * 数据流方向，向内或向外
	 */
	private Boolean direction;
	
	/**
	 * 分页
	 */
	private Page page;
	
	/**
	 * 协议类型集合
	 */
	private String[] prot_types;
	
	/**
	 * 源IP集合
	 */
	private String[] src_ips;
	
	/**
	 * 目的IP集合
	 */
	private String[] dest_ips;
	
	/**
	 * 目的端口集合
	 */
	private String[] dest_ports;
	
	private Integer bureauId;

	public Integer getBureauId() {
		return bureauId;
	}

	public void setBureauId(Integer bureauId) {
		this.bureauId = bureauId;
	}

	public String[] getProt_types() {
		return prot_types;
	}

	public void setProt_types(String[] prot_types) {
		this.prot_types = prot_types;
	}

	public String[] getSrc_ips() {
		return src_ips;
	}

	public void setSrc_ips(String[] src_ips) {
		this.src_ips = src_ips;
	}

	public String[] getDest_ips() {
		return dest_ips;
	}

	public void setDest_ips(String[] dest_ips) {
		this.dest_ips = dest_ips;
	}

	public String[] getDest_ports() {
		return dest_ports;
	}

	public void setDest_ports(String[] dest_ports) {
		this.dest_ports = dest_ports;
	}

	public Timestamp getStarttime() {
		return starttime;
	}
	
	public void setStarttime(Timestamp starttime) {
		this.starttime = starttime;
	}

	public Timestamp getEndtime() {
		return endtime;
	}

	public void setEndtime(Timestamp endtime) {
		this.endtime = endtime;
	}

	public String getSrc_ip() {
		return src_ip;
	}

	public void setSrc_ip(String src_ip) {
		this.src_ip = src_ip;
	}

	public Integer getSrc_port() {
		return src_port;
	}

	public void setSrc_port(Integer src_port) {
		this.src_port = src_port;
	}

	public Integer getDest_port() {
		return dest_port;
	}

	public void setDest_port(Integer dest_port) {
		this.dest_port = dest_port;
	}

	public Integer getThre_rank() {
		return thre_rank;
	}

	public void setThre_rank(Integer thre_rank) {
		this.thre_rank = thre_rank;
	}

	public String getFaci_ip() {
		return faci_ip;
	}

	public void setFaci_ip(String faci_ip) {
		this.faci_ip = faci_ip;
	}

	public String getFaci_type() {
		return faci_type;
	}

	public void setFaci_type(String faci_type) {
		this.faci_type = faci_type;
	}

	public String getEvent_type() {
		return event_type;
	}

	public void setEvent_type(String event_type) {
		this.event_type = event_type;
	}

	public String getDescrip() {
		return descrip;
	}

	public void setDescrip(String descrip) {
		this.descrip = descrip;
	}

	public Boolean getDirection() {
		return direction;
	}

	public void setDirection(Boolean direction) {
		this.direction = direction;
	}

	public Page getPage() {
		return page;
	}

	public void setPage(Page page) {
		this.page = page;
	}

}
