package org.infosec.ismp.manager.rmi.wsm.model;

import java.io.Serializable;
/**
 * siteCheckRmi接口传输对象
 * @author jiel
 *
 */
public class SiteCheckRmiModel implements Serializable {

	
	private String nodeid;
	private String url;
	private String pingStatus;//在线状态
	private String siteCheckStatus;//检测状态
	private double responseTime; //相应返回时间
	private String siteCheckTime;//检测时间
	public String getNodeid() {
		return nodeid;
	}
	public void setNodeid(String nodeid) {
		this.nodeid = nodeid;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getPingStatus() {
		return pingStatus;
	}
	public void setPingStatus(String pingStatus) {
		this.pingStatus = pingStatus;
	}
	public String getSiteCheckStatus() {
		return siteCheckStatus;
	}
	public void setSiteCheckStatus(String siteCheckStatus) {
		this.siteCheckStatus = siteCheckStatus;
	}
	public double getResponseTime() {
		return responseTime;
	}
	public void setResponseTime(double responseTime) {
		this.responseTime = responseTime;
	}
	public String getSiteCheckTime() {
		return siteCheckTime;
	}
	public void setSiteCheckTime(String siteCheckTime) {
		this.siteCheckTime = siteCheckTime;
	}
	
	

}
